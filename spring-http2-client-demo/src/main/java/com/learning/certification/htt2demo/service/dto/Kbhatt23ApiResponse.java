package com.learning.certification.htt2demo.service.dto;

public class Kbhatt23ApiResponse {

	private String status;

	public Kbhatt23ApiResponse(String status) {
		super();
		this.status = status;
	}

	public Kbhatt23ApiResponse() {
		super();
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return "Kbhatt23ApiResponse [status=" + status + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((status == null) ? 0 : status.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Kbhatt23ApiResponse other = (Kbhatt23ApiResponse) obj;
		if (status == null) {
			if (other.status != null)
				return false;
		} else if (!status.equals(other.status))
			return false;
		return true;
	}
	
	
}
